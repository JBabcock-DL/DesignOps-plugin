#!/usr/bin/env node
// Minify the two large `.figma.js` templates that /create-component inlines into
// every `use_figma` call. Minification preserves function names (esbuild
// `keepNames: true`) because the runtime `typeof X === 'function'` assertions
// at draw-engine.figma.js §6.2a / §6.9a look up helpers by their source name.
//
// Inputs:
//   skills/create-component/templates/draw-engine.figma.js
//   skills/create-component/templates/archetype-builders.figma.js
//
// Outputs (siblings, committed):
//   skills/create-component/templates/draw-engine.min.figma.js
//   skills/create-component/templates/archetype-builders.min.figma.js
//
// Usage:
//   node scripts/build-min-templates.mjs           # writes .min siblings
//   node scripts/build-min-templates.mjs --check   # non-zero if any .min is stale
//
// The .min siblings are committed so agents don't need Node installed to run
// /create-component — the workflow reads and inlines the pre-built .min file.
// `verify-cache.sh` enforces that every *.figma.js newer than its .min sibling
// causes a non-zero exit.

import { readFileSync, writeFileSync, existsSync, statSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve, relative } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');

const TEMPLATES = [
  'skills/create-component/templates/draw-engine.figma.js',
  'skills/create-component/templates/archetype-builders.figma.js',
];

async function loadEsbuild() {
  try {
    return await import('esbuild');
  } catch (err) {
    console.error(
      'error: esbuild is required but not installed.\n' +
        '       run `npm install` at the repo root to install devDependencies,\n' +
        '       then retry `npm run build:min`.'
    );
    process.exit(1);
  }
}

function minifiedPath(src) {
  return src.replace(/\.figma\.js$/, '.min.figma.js');
}

async function buildOne(esbuild, srcRel) {
  const srcAbs = resolve(REPO_ROOT, srcRel);
  const destAbs = resolve(REPO_ROOT, minifiedPath(srcRel));
  const srcText = readFileSync(srcAbs, 'utf8');

  // The Figma plugin sandbox wraps `use_figma` payloads in an implicit
  // `(async () => { ... })()`. That lets the templates use top-level `await`
  // AND top-level `return` simultaneously — but esbuild can't parse either at
  // the module top level. Wrap in the same async IIFE shape before
  // minification, then strip the wrapper so the output is still a plain
  // script body that /create-component can inline verbatim.
  const WRAP_OPEN = '(async()=>{';
  const WRAP_CLOSE = '})();';
  const wrapped = `${WRAP_OPEN}\n${srcText}\n${WRAP_CLOSE}`;

  // We strip whitespace + comments + dead-code syntax, but DO NOT mangle
  // identifiers. The runtime `typeof buildSurfaceStackVariant === 'function'`
  // assertions at draw-engine §6.2a / §6.9a resolve by source name, and
  // identifier renaming would either rename the function (assertion throws a
  // false positive) or inject esbuild's `__name` keepNames helper at module
  // scope (which then lives outside our async-IIFE wrapper and can't be
  // peeled cleanly). Keeping identifiers gives us the main compression win
  // (comments + whitespace = the bulk of the file) without either risk.
  const result = await esbuild.transform(wrapped, {
    minifyWhitespace: true,
    minifySyntax: true,
    minifyIdentifiers: false,
    loader: 'js',
    target: 'esnext',
    legalComments: 'none',
  });

  let minBody = result.code.trim();
  if (minBody.endsWith(';')) minBody = minBody.slice(0, -1);
  // esbuild can reshape the wrapper slightly (e.g. `(async()=>{...})()` or
  // `(async()=>{...})();`). Peel whichever form we get.
  const WRAP_PREFIXES = ['(async()=>{', '(async () => {'];
  const WRAP_SUFFIXES = ['})()', '})();'];
  let peeled = minBody;
  const matchedPrefix = WRAP_PREFIXES.find(p => peeled.startsWith(p));
  if (!matchedPrefix) {
    throw new Error(
      `minify wrapper not found in output for ${srcRel} — cannot safely strip.\n` +
        `first 80 chars: ${peeled.slice(0, 80)}`
    );
  }
  peeled = peeled.slice(matchedPrefix.length);
  const matchedSuffix = WRAP_SUFFIXES.find(s => peeled.endsWith(s));
  if (!matchedSuffix) {
    throw new Error(
      `minify wrapper close not found in output for ${srcRel} — cannot safely strip.\n` +
        `last 80 chars: ${peeled.slice(-80)}`
    );
  }
  peeled = peeled.slice(0, peeled.length - matchedSuffix.length);

  // Runtime assertions and sync-cache rely on a predictable first line: a tiny
  // banner that names the source file so a developer opening the .min file
  // knows it is generated.
  const banner =
    `// GENERATED by scripts/build-min-templates.mjs — do not edit by hand.\n` +
    `// source: ${srcRel}\n` +
    `// regenerate with: npm run build:min\n`;

  const payload = banner + peeled + '\n';
  writeFileSync(destAbs, payload);
  return {
    srcRel,
    destRel: minifiedPath(srcRel),
    srcBytes: Buffer.byteLength(srcText, 'utf8'),
    destBytes: Buffer.byteLength(payload, 'utf8'),
  };
}

async function checkOne(srcRel) {
  const srcAbs = resolve(REPO_ROOT, srcRel);
  const destAbs = resolve(REPO_ROOT, minifiedPath(srcRel));
  if (!existsSync(destAbs)) {
    return { srcRel, missing: true };
  }
  const srcMtime = statSync(srcAbs).mtimeMs;
  const destMtime = statSync(destAbs).mtimeMs;
  if (srcMtime > destMtime) {
    return { srcRel, stale: true };
  }
  return { srcRel, ok: true };
}

async function main() {
  const check = process.argv.includes('--check');

  if (check) {
    const results = await Promise.all(TEMPLATES.map(checkOne));
    let failed = 0;
    for (const r of results) {
      if (r.missing) {
        console.error(`drift: ${minifiedPath(r.srcRel)} is missing. run npm run build:min`);
        failed++;
      } else if (r.stale) {
        console.error(
          `drift: ${r.srcRel} is newer than its .min sibling. run npm run build:min`
        );
        failed++;
      }
    }
    if (failed > 0) process.exit(1);
    console.log('build-min-templates: OK (all up to date)');
    return;
  }

  const esbuild = await loadEsbuild();
  const results = [];
  for (const srcRel of TEMPLATES) {
    const r = await buildOne(esbuild, srcRel);
    results.push(r);
    const pct = ((1 - r.destBytes / r.srcBytes) * 100).toFixed(1);
    console.log(
      `minified ${r.srcRel}\n` +
        `  -> ${r.destRel} (${r.srcBytes} -> ${r.destBytes} bytes, -${pct}%)`
    );
  }
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
